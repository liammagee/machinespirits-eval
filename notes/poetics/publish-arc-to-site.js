#!/usr/bin/env node
/* publish-arc-to-site.js — stage the dramatic-recognition arc note for machinespirits.org.

   The arc note is published the same way as the other ai-tutor explainers
   (e.g. articles/ai-tutor/geist-explained.html): a self-contained, hand-authored
   HTML article in the *content-philosophy* repo. That repo's ./publish triggers
   the website's Fly redeploy (a `content-updated` GitHub dispatch), so the
   website itself never receives the file directly. This script does the
   cross-repo staging that's otherwise easy to get wrong:

     1. bundle the linked source into a self-contained standalone (CSS+JS inlined,
        via package-standalone.js's bundler) so it needs no sibling assets/ on the site
     2. public-ise it: neutralise the localhost :3466 live-data layer (beacon,
        TOC readout, "live · :3466" rune, run-count deeplinks) so the page reads as
        a static snapshot — or --live-base to point the deeplinks at a deployed browser
     3. write a frontmatter stub <slug>.md and BACKDATE its mtime a minute, so the
        hand-authored <slug>.html is unambiguously newer and content-philosophy's
        ./build (which skips only when `.html -nt .md`, at 1-second granularity)
        never Pandoc-clobbers the page. NOTE the page SERVES at the static content
        path /content/articles/ai-tutor/<slug>.html (same as geist-explained), NOT
        an /essays/ route — the .md only carries title/date/theme/dek for any index
        that consumes it.
     4. copy exactly the cartoon PNGs the page references into articles/ai-tutor/images/
     5. print the ./publish command — the actual outward deploy is human-gated
        (only --publish runs it)

   Usage:
     node notes/poetics/publish-arc-to-site.js                 # stage (no deploy)
     node notes/poetics/publish-arc-to-site.js --dry-run       # print plan, write nothing
     node notes/poetics/publish-arc-to-site.js --publish       # stage + run ./publish (DEPLOYS LIVE)
     node notes/poetics/publish-arc-to-site.js --slug NAME     # output basename (default below)
     node notes/poetics/publish-arc-to-site.js --live-base URL # point :3466 links at a real browser
     node notes/poetics/publish-arc-to-site.js --no-publicize  # copy verbatim (live layer left localhost)
*/
import { readFileSync, writeFileSync, copyFileSync, mkdirSync, existsSync, utimesSync } from 'node:fs';
import { dirname, resolve, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { execFileSync } from 'node:child_process';
import { bundleStandalone } from './package-standalone.js';

const HERE = dirname(fileURLToPath(import.meta.url)); // notes/poetics
const REPO = resolve(HERE, '../..'); // machinespirits-eval-dramatic
const SRC = join(HERE, '2026-05-26-paper-to-dramatic-recognition-arc.html');
const IMG_SRC = join(HERE, 'images');
const DEST_REPO = resolve(REPO, '../machinespirits-content-philosophy');
const DEST_DIR = join(DEST_REPO, 'articles', 'ai-tutor');
const DEST_IMG = join(DEST_DIR, 'images');

const args = process.argv.slice(2);
const has = (f) => args.includes(f);
const val = (f, d) => {
  const i = args.indexOf(f);
  return i >= 0 && args[i + 1] && !args[i + 1].startsWith('--') ? args[i + 1] : d;
};
const DRY = has('--dry-run');
const DO_PUBLISH = has('--publish');
const PUBLICIZE = !has('--no-publicize');
const SLUG = val('--slug', 'dramatic-recognition-arc');
const LIVE_BASE = val('--live-base', '');
const DATE = '2026-05-26';

const DEST_HTML = join(DEST_DIR, `${SLUG}.html`);
const DEST_MD = join(DEST_DIR, `${SLUG}.md`);

// Neutralise (or re-target) the localhost :3466 live-data layer for public hosting.
function publicize(html) {
  if (!PUBLICIZE) return html;
  if (LIVE_BASE) {
    return html.replaceAll('http://127.0.0.1:3466', LIVE_BASE.replace(/\/+$/, ''));
  }
  return html
    .replace(/href="http:\/\/127\.0\.0\.1:3466[^"]*"/g, 'href="#" data-live-disabled="1"')
    .replace(/live · :3466/g, 'snapshot') // beacon badge, live branch
    .replace(/offline · :3466/g, 'snapshot') // beacon badge, offline branch (the one that actually fires over https)
    .replace(/127\.0\.0\.1:3466 — probing…/g, 'static snapshot')
    .replace(/127\.0\.0\.1:3466/g, 'the poetics browser');
}

// /essays index metadata only — NOT the page body. Backdated below (mtime set older
// than the .html) so content-philosophy's ./build leaves the hand-authored .html alone.
const frontmatterStub = `---
title: "From Paper 2.0 to dramatic recognition — arc to date"
date: ${DATE}
theme: ai-tutor
dek: "Staging the tutoring dialogue as drama: can a tutor answer a learner's specific stuck point with a visible change of move? A live, data-driven synthesis note."
---

<!-- The page itself is the sibling ${SLUG}.html — a self-contained, hand-authored
     techne document generated by notes/poetics/publish-arc-to-site.js in the
     machinespirits-eval-dramatic repo. This .md exists ONLY to give /essays its
     title/date/theme/dek and is intentionally kept older than the .html so
     ./build never Pandoc-renders over the hand-authored page. Do not edit the
     .html here — edit the source note and re-run the publish script. -->
`;

// Copy exactly the images the page references (future-proof if panels change).
const referenced = [...new Set([...readFileSync(SRC, 'utf8').matchAll(/src="images\/([^"]+)"/g)].map((m) => m[1]))];

const bundled = publicize(bundleStandalone(SRC));

console.log(`arc → machinespirits.org   [${DRY ? 'DRY RUN' : DO_PUBLISH ? 'STAGE + PUBLISH' : 'STAGE ONLY'}]`);
console.log(`  source    ${SRC}`);
console.log(`  html   →  ${DEST_HTML}`);
console.log(
  `            ${(bundled.length / 1024).toFixed(0)} KB · public-ize=${PUBLICIZE}${LIVE_BASE ? ` · live-base=${LIVE_BASE}` : ''}`,
);
console.log(`  md     →  ${DEST_MD}  (frontmatter stub, written first)`);
console.log(`  images →  ${DEST_IMG}/  (${referenced.length} files)`);
referenced.forEach((f) => console.log(`              ${existsSync(join(IMG_SRC, f)) ? '·' : '✗ MISSING'} ${f}`));
console.log(`  public URL after deploy:  https://machinespirits.org/content/articles/ai-tutor/${SLUG}.html`);

if (DRY) {
  console.log('\n(dry run — nothing written)');
  process.exit(0);
}

if (!existsSync(DEST_REPO)) {
  console.error(`\n✗ content-philosophy repo not found at ${DEST_REPO}`);
  process.exit(1);
}

mkdirSync(DEST_IMG, { recursive: true });
writeFileSync(DEST_MD, frontmatterStub);
writeFileSync(DEST_HTML, bundled);
// content-philosophy's ./build skips a .md only when `.html -nt .md` — and bash `-nt`
// compares mtimes at 1-SECOND granularity. Writing both back-to-back (same wall-clock
// second) reads as "not newer", so Pandoc renders the stub OVER the hand-authored .html.
// Backdate the .md a full minute so the .html is unambiguously newer and ./build leaves it.
const mdOld = new Date(Date.now() - 60_000);
utimesSync(DEST_MD, mdOld, mdOld);
let copied = 0;
for (const f of referenced) {
  const from = join(IMG_SRC, f);
  if (!existsSync(from)) {
    console.error(`  ✗ missing image, skipped: ${from}`);
    continue;
  }
  copyFileSync(from, join(DEST_IMG, f));
  copied += 1;
}
console.log(`\n✓ staged into ${DEST_REPO} (${copied}/${referenced.length} images)`);

if (DO_PUBLISH) {
  console.log('\n→ content-philosophy ./publish (deploys to machinespirits.org, ~3-5 min)…\n');
  execFileSync('./publish', [`Add dramatic-recognition arc note (${SLUG})`], {
    cwd: DEST_REPO,
    stdio: 'inherit',
  });
} else {
  console.log('\nNext — deploys to machinespirits.org (~3-5 min):');
  console.log(`  cd ${DEST_REPO} && ./publish "Add dramatic-recognition arc note"`);
  console.log('  …or re-run this script with --publish');
}
