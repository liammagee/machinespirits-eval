const CONCEPT_BY_ID = new Map(DERIVATION_SEMANTIC_CONCEPTS.map((concept) => [concept.id, concept]));

export function getDerivationConceptById(id) {
  return CONCEPT_BY_ID.get(id) || null;
}

export function getDerivationConceptLinks() {
  return DERIVATION_SEMANTIC_CONCEPTS.flatMap((concept) =>
    concept.links.map((link) => ({
      source: concept.id,
      sourceLabel: concept.label,
      ...link,
      targetLabel: getDerivationConceptById(link.target)?.label || link.target,
    })),
  );
}

export function getDerivationConceptSchema() {
  return {
    ontologyModule: DERIVATION_CONCEPT_ONTOLOGY_MODULE,
    ontologyModules: DERIVATION_CONCEPT_ONTOLOGY_MODULES,
    layers: DERIVATION_CONCEPT_LAYERS,
    vocabularyGroups: DERIVATION_VOCABULARY_GROUPS,
    acts: DERIVATION_CONCEPT_ACTS,
    novelConceptIds: DERIVATION_NOVEL_CONCEPT_IDS,
    concepts: DERIVATION_SEMANTIC_CONCEPTS,
    links: getDerivationConceptLinks(),
  };
}
