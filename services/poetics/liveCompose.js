/**
 * Live "sit-in" compose engine — a human plays ONE seat (tutor or learner) and
 * the AI plays the other, turn by turn, in real time. This is the interactive
 * cousin of services/pilotAutoplay.js: autoplay drives BOTH seats as AI to make
 * a transcript unattended; here a human occupies one chair and drives the tempo.
 *
 * Why it exists: hand-probing the instrument. Human-as-learner is the familiar
 * direction (it is exactly what /pilot does). Human-as-TUTOR is the new lever —
 * you can interrogate whether a learner persona behaves, or deliberately stage a
 * *performed* recognition to test judge-gullibility (paper §D6). A played
 * transcript can be saved as an artifact and later run through the critics — the
 * "debugging validation" payoff.
 *
 * Both AI seats are generated by the SAME engines the live chat + pilot use, so
 * the sit-in tutor/learner cannot drift from the scored ones:
 *   - AI tutor   → runTutorTurn()           (routes/chatRoutes.js — carries the
 *                                             no-fake-simulations affordance too)
 *   - AI learner → generateLearnerResponse() (services/learnerTutorInteractionEngine.js)
 *
 * METERED: every AI turn is a real LLM call (unless mock deps are injected).
 * State lives in-process (single-user localhost tool); nothing is written to the
 * eval DB. saveSession() writes a self-describing transcript artifact only.
 */

