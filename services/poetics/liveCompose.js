  "Honestly I just want the rule before the quiz. Logs are the opposite of powers, I guess? I don't really trust 'inverse'.",
  "Wait — so log₂(8) is asking what power of 2 makes 8? That's 3. Okay, that's less hand-wavy than I thought.",
  "Two questions, I think? But I keep wanting to split it like log a + log b. That's the part that bites me.",
  "log₂(8) is 3, and log₂(4)+log₂(2) is 2+1 = 3 — wait, those match? No—that's 4×2=8, that's multiply not add.",
