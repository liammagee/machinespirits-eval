/**
 * Live "sit-in" compose engine — a human plays ONE seat (tutor or learner) and
 * the AI plays the other, turn by turn, in real time. This is the interactive
 * cousin of services/pilotAutoplay.js: autoplay drives BOTH seats as AI to make
 * a transcript unattended; here a human occupies one chair and drives the tempo.
 *
 * Why it exists: hand-probing the instrument. Human-as-learner is the familiar
 * direction (it is exactly what /pilot does). Human-as-TUTOR is the new lever —
 * you can interrogate whether a learner persona behaves, or deliberately stage a
 * *performed* recognition to test judge-gullibility (paper §D6). A played
 * transcript can be saved as an artifact and later run through the critics — the
 * "debugging validation" payoff.
 *
 * Both AI seats are generated by the SAME engines the live chat + pilot use, so
 * the sit-in tutor/learner cannot drift from the scored ones:
 *   - AI tutor   → runTutorTurn()           (routes/chatRoutes.js — carries the
 *                                             no-fake-simulations affordance too)
 *   - AI learner → generateLearnerResponse() (services/learnerTutorInteractionEngine.js)
 *
 * METERED: every AI turn is a real LLM call (unless mock deps are injected).
 * State lives in-process (single-user localhost tool); nothing is written to the
 * eval DB. saveSession() writes a self-describing transcript artifact only.
 */

import crypto from 'node:crypto';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import YAML from 'yaml';

import * as evalConfigLoader from '../evalConfigLoader.js';
import interactionEngine from '../learnerTutorInteractionEngine.js';
import { runTutorTurn } from '../../routes/chatRoutes.js';

const __filename = fileURLToPath(import.meta.url);
const ROOT = path.resolve(path.dirname(__filename), '../..');

export const ROLES = Object.freeze({ TUTOR: 'tutor', LEARNER: 'learner' });
export const DEFAULT_MAX_TURNS = 16;
export const MAX_TURNS_CEILING = 40;

const CHAT_MIN_MAX_TOKENS = 4000;
const MAX_TEXT_LEN = 8000;
const MAX_LIVE_SESSIONS = 50; // evict oldest beyond this — localhost, single user
const CONSERVATIVE_USD_PER_1K_TOKENS = 0.01; // safety-biased backstop, not accounting

// Map the live config (prompt_type × tutor architecture) to a representative
// registered cell — runTutorTurn just needs a tutor-agents profile object, and
// cells ARE those profiles. Only the cell's TUTOR config is used here; its
// learner side is irrelevant (the AI learner is built from the spec instead).
// All four are registered in evaluationRunner.js EVAL_ONLY_PROFILES.
const TUTOR_CELL = Object.freeze({
  'recognition|ego_superego': 'cell_7_recog_multi_unified',
  'recognition|ego_only': 'cell_5_recog_single_unified',
  'base|ego_superego': 'cell_3_base_multi_unified',
  'base|ego_only': 'cell_1_base_single_unified',
});

export function tutorCellFor(promptType, architecture) {
  const pt = promptType === 'base' ? 'base' : 'recognition';
  const arch = architecture === 'ego_superego' ? 'ego_superego' : 'ego_only';
  return TUTOR_CELL[`${pt}|${arch}`];
}

// ── Session store (in-memory) ───────────────────────────────────────────────

const SESSIONS = new Map();

function evictIfNeeded() {
  while (SESSIONS.size > MAX_LIVE_SESSIONS) {
    const oldest = SESSIONS.keys().next().value;
    SESSIONS.delete(oldest);
  }
}

function liveError(message, code, statusCode = 400) {
  const err = new Error(message);
  err.code = code;
  err.statusCode = statusCode;
  return err;
}

function getSession(id) {
  const s = SESSIONS.get(id);
  if (!s) throw liveError(`live session not found: ${id}`, 'LIVE_SESSION_NOT_FOUND', 404);
  return s;
}

function estimateCost(spend) {
  return ((spend.inputTokens + spend.outputTokens) / 1000) * CONSERVATIVE_USD_PER_1K_TOKENS;
}

// A public, blinding-free view of the session for the client. (Nothing secret
// here — but it keeps the wire shape stable + omits internals like deps.)
function sessionView(s) {
  return {
    id: s.id,
    topic: s.topic,
    hamartia: s.hamartia,
    humanRole: s.humanRole,
    aiRole: s.aiRole,
    persona: s.persona,
    learnerArchitecture: s.learnerArchitecture,
    promptType: s.promptType,
    tutorArchitecture: s.tutorArchitecture,
    tutorCell: s.tutorCell,
    nextSpeaker: s.nextSpeaker,
    status: s.status,
    stoppedReason: s.stoppedReason,
    maxTurns: s.maxTurns,
    turnCount: s.transcript.length,
    transcript: s.transcript.map(publicTurn),
    spend: { ...s.spend },
  };
}

// The human never needs the AI's private deliberation mid-play (it would leak
// the learner's interior — the very thing the instrument is supposed to read
// only from the surface). Keep it in the stored turn for the saved artifact,
// but strip it from the live wire view.
function publicTurn(t) {
  return { role: t.role, by: t.by, text: t.text };
}

// ── AI turn generation ──────────────────────────────────────────────────────

// Minimal OpenRouter learner adapter — mirrors the one in services/pilotAutoplay.js
// (same single-metered-provider posture); kept local so this engine doesn't
// depend on the pilot's internals.
async function openRouterLearnerCall(apiKey, modelRef, systemPrompt, messages, options = {}) {
  let modelId = modelRef;
  if (!modelRef) {
    modelId = 'nvidia/nemotron-3-nano-30b-a3b';
  } else if (!modelRef.includes('/') && modelRef.includes('.')) {
    modelId = evalConfigLoader.resolveModel(modelRef).model;
  }
  const start = Date.now();
  const response = await fetch('https://openrouter.ai/api/v1/chat/completions', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      Authorization: `Bearer ${apiKey}`,
      'HTTP-Referer': 'http://localhost:3466/compose/live',
      'X-Title': 'Machine Spirits Live Compose (AI learner)',
    },
    body: JSON.stringify({
      model: modelId,
      temperature: options.temperature ?? 0.7,
      max_tokens: Math.max(options.maxTokens ?? CHAT_MIN_MAX_TOKENS, CHAT_MIN_MAX_TOKENS),
      messages: [
        { role: 'system', content: systemPrompt },
        ...(messages || []).map((m) => ({
          role: m.role === 'user' ? 'user' : 'assistant',
          content: m.content,
        })),
      ],
    }),
  });
  const latencyMs = Date.now() - start;
  if (!response.ok) {
    const body = await response.text().catch(() => '');
    throw liveError(`OpenRouter ${response.status}: ${body.slice(0, 200)}`, 'LIVE_LLM_ERROR', 502);
  }
  const payload = await response.json();
  return {
    content: payload.choices?.[0]?.message?.content || '',
    usage: {
      inputTokens: payload.usage?.prompt_tokens || 0,
      outputTokens: payload.usage?.completion_tokens || 0,
    },
    model: modelId,
    latencyMs,
  };
}

// Default AI-tutor turn — the real ego(/superego) tutor. history must NOT
// include the triggering learner message; runTutorTurn takes it separately.
async function defaultTutorTurn({ session, learnerMessage, history, apiKey }) {
  const profile = evalConfigLoader.loadTutorAgents()?.profiles?.[session.tutorCell];
  if (!profile) {
    throw liveError(`unknown tutor cell '${session.tutorCell}'`, 'LIVE_BAD_TUTOR_CELL', 500);
  }
  const trace = await runTutorTurn({
    profile,
    apiKey,
    history,
    learnerMessage,
    topic: session.topic,
    curriculum: null,
    useClaudeCli: false,
  });
  return {
    text: trace.finalMessage || '',
    deliberation: trace.deliberation || [],
    usage: { inputTokens: trace.totals?.inputTokens || 0, outputTokens: trace.totals?.outputTokens || 0 },
  };
}

// Default AI-learner turn — the real ego/superego learner, built from the SPEC's
// persona + architecture (not from the tutor cell). Returns the external message
// plus the internal deliberation (kept for the saved artifact, stripped from the
// live view).
async function defaultLearnerTurn({ session, tutorMessage, history, apiKey }) {
  const trace = { metrics: { learnerInputTokens: 0, learnerOutputTokens: 0 } };
  const llmCall = (modelRef, systemPrompt, messages, options = {}) =>
    openRouterLearnerCall(apiKey, modelRef || session.model, systemPrompt, messages, options);
  const res = await interactionEngine.generateLearnerResponse({
    tutorMessage,
    topic: session.topic,
    conversationHistory: history,
    learnerProfile: session.learnerArchitecture,
    personaId: session.persona,
    llmCall,
    trace,
  });
  return {
    text: res.externalMessage || res.message || '',
    deliberation: res.internalDeliberation || [],
    suggestsEnding: !!res.suggestsEnding,
    usage: {
      inputTokens: trace.metrics.learnerInputTokens || 0,
      outputTokens: trace.metrics.learnerOutputTokens || 0,
    },
  };
}

// Generate one AI turn for whichever seat the AI holds, using the transcript so
// far. The human seat must NOT be next (callers guard turn order).
async function generateAiTurn(session, deps) {
  const apiKey = deps.apiKey ?? process.env.OPENROUTER_API_KEY;
  if (!apiKey && !deps.tutorTurnFn && !deps.learnerTurnFn) {
    throw liveError('OPENROUTER_API_KEY is not set (and no mock deps injected)', 'LIVE_NO_API_KEY', 503);
  }
  const history = session.transcript.map((t) => ({ role: t.role, content: t.text }));

  if (session.aiRole === ROLES.TUTOR) {
    // The tutor responds to the most recent learner turn (or a seed opener).
    const last = session.transcript[session.transcript.length - 1];
    const learnerMessage =
      last && last.role === ROLES.LEARNER ? last.text : `Let's begin — we're here to work through ${session.topic}.`;
    const priorHistory = last && last.role === ROLES.LEARNER ? history.slice(0, -1) : history;
    const fn = deps.tutorTurnFn || defaultTutorTurn;
    const out = await fn({ session, learnerMessage, history: priorHistory, apiKey });
    return { role: ROLES.TUTOR, by: 'ai', ...out };
  }

  // AI is the learner: responds to the most recent tutor turn (or a seed opener).
  const last = session.transcript[session.transcript.length - 1];
  const tutorMessage =
    last && last.role === ROLES.TUTOR
      ? last.text
      : `Let's get started. We're going to work on ${session.topic}. What's on your mind?`;
  const fn = deps.learnerTurnFn || defaultLearnerTurn;
  const out = await fn({ session, tutorMessage, history, apiKey });
  return { role: ROLES.LEARNER, by: 'ai', ...out };
}

function applyTurn(session, turn) {
  session.transcript.push(turn);
  if (turn.usage) {
    session.spend.inputTokens += turn.usage.inputTokens || 0;
    session.spend.outputTokens += turn.usage.outputTokens || 0;
    session.spend.estimatedCostUsd = estimateCost(session.spend);
  }
  session.nextSpeaker = turn.role === ROLES.TUTOR ? ROLES.LEARNER : ROLES.TUTOR;
  if (session.transcript.length >= session.maxTurns) {
    session.status = 'done';
    session.stoppedReason = 'max_turns';
  } else if (turn.by === 'ai' && turn.role === ROLES.LEARNER && turn.suggestsEnding) {
    session.status = 'done';
    session.stoppedReason = 'learner_ended';
  }
  return turn;
}

// ── Public API ──────────────────────────────────────────────────────────────

/**
 * Start a live session. If the opening speaker is the AI seat, the opening AI
 * turn is generated immediately so the human arrives to a line already on stage.
 * Returns { session, openingTurn|null }.
 */
export async function startSession(spec = {}, deps = {}) {
  const humanRole = spec.humanRole === ROLES.TUTOR ? ROLES.TUTOR : ROLES.LEARNER;
  const aiRole = humanRole === ROLES.TUTOR ? ROLES.LEARNER : ROLES.TUTOR;
  const openingSpeaker = spec.openingSpeaker === ROLES.TUTOR ? ROLES.TUTOR : ROLES.LEARNER;
  const maxTurns = Math.min(Math.max(Number(spec.maxTurns) || DEFAULT_MAX_TURNS, 2), MAX_TURNS_CEILING);

  const session = {
    id: `live_${crypto.randomBytes(6).toString('hex')}`,
    topic: String(spec.topic || 'logarithms as the inverse of exponentiation').slice(0, 500),
    hamartia: String(spec.hamartia || '').slice(0, 500),
    humanRole,
    aiRole,
    persona: spec.persona || 'struggling_anxious',
    learnerArchitecture: spec.learnerArchitecture || 'ego_superego_recognition_authentic',
    promptType: spec.promptType || 'recognition',
    tutorArchitecture: spec.tutorArchitecture === 'ego_superego' ? 'ego_superego' : 'ego_only',
    tutorCell: tutorCellFor(spec.promptType, spec.tutorArchitecture),
    model: spec.model || null,
    openingSpeaker,
    maxTurns,
    transcript: [],
    nextSpeaker: openingSpeaker,
    status: 'live',
    stoppedReason: null,
    spend: { inputTokens: 0, outputTokens: 0, estimatedCostUsd: 0 },
    createdAt: Date.now(),
  };
  SESSIONS.set(session.id, session);
  evictIfNeeded();

  let openingTurn = null;
  if (openingSpeaker === aiRole) {
    openingTurn = applyTurn(session, await generateAiTurn(session, deps));
  }
  return { session: sessionView(session), openingTurn: openingTurn ? publicTurn(openingTurn) : null };
}

/**
 * Append the human's turn, then generate the AI's reply. Returns
 * { humanTurn, aiTurn|null, session }. aiTurn is null only if the session hit
 * its turn cap on the human turn.
 */
export async function humanTurn(sessionId, text, deps = {}) {
  const session = getSession(sessionId);
  if (session.status !== 'live') {
    throw liveError(`session is '${session.status}', not accepting turns`, 'LIVE_SESSION_CLOSED', 409);
  }
  if (session.nextSpeaker !== session.humanRole) {
    throw liveError(`it is the ${session.aiRole}'s turn, not yours`, 'LIVE_NOT_YOUR_TURN', 409);
  }
  const clean = String(text || '').trim();
  if (!clean) throw liveError('empty turn', 'LIVE_EMPTY_TURN', 400);
  if (clean.length > MAX_TEXT_LEN) throw liveError('turn too long', 'LIVE_TEXT_TOO_LONG', 413);

  const human = applyTurn(session, { role: session.humanRole, by: 'human', text: clean });

  let ai = null;
  if (session.status === 'live') {
    ai = applyTurn(session, await generateAiTurn(session, deps));
  }
  return {
    humanTurn: publicTurn(human),
    aiTurn: ai ? publicTurn(ai) : null,
    session: sessionView(session),
  };
}

/** Read-only session view (for reconnect / polling). */
export function viewSession(sessionId) {
  return sessionView(getSession(sessionId));
}

const SAVE_DIR = path.resolve(ROOT, 'exports/live-compose');

function safeSlug(s) {
  return String(s || '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .slice(0, 60);
}

/**
 * Persist the played transcript as a self-describing artifact (spec + ordered
 * turns, learner deliberation included). v1 writes to exports/live-compose/ and
 * returns the path; running the critics over saved transcripts is the immediate
 * follow-up (the artifact carries everything that pipeline needs).
 */
export function saveSession(sessionId, opts = {}) {
  const session = getSession(sessionId);
  fs.mkdirSync(SAVE_DIR, { recursive: true });
  const base = safeSlug(opts.filename || `${session.id}-${session.topic}`) || session.id;
  const dest = path.resolve(SAVE_DIR, `${base}.yaml`);
  if (!dest.startsWith(`${SAVE_DIR}${path.sep}`)) {
    throw liveError('unsafe filename', 'LIVE_UNSAFE_FILENAME', 400);
  }
  const artifact = {
    kind: 'live-compose-transcript',
    id: session.id,
    created_at: new Date(session.createdAt).toISOString(),
    spec: {
      topic: session.topic,
      hamartia: session.hamartia,
      human_role: session.humanRole,
      ai_role: session.aiRole,
      persona: session.persona,
      learner_architecture: session.learnerArchitecture,
      prompt_type: session.promptType,
      tutor_architecture: session.tutorArchitecture,
      tutor_cell: session.tutorCell,
    },
    spend: { ...session.spend },
    stopped_reason: session.stoppedReason,
    transcript: session.transcript.map((t) => ({
      role: t.role,
      by: t.by,
      text: t.text,
      ...(t.deliberation && t.deliberation.length ? { deliberation: t.deliberation } : {}),
    })),
  };
  const yamlText = YAML.stringify(artifact);
  fs.writeFileSync(dest, yamlText, 'utf8');
  return { path: path.relative(ROOT, dest), bytes: Buffer.byteLength(yamlText) };
}

