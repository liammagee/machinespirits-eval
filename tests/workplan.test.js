// Hermetic tests for the workplan CLI (scripts/workplan.js).
// Runs the real CLI against a throwaway WORKPLAN_DIR so the production board is
// never touched. No DB, no network, no API.
import { test } from 'node:test';
import assert from 'node:assert/strict';
import { spawnSync } from 'node:child_process';
import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const CLI = path.join(ROOT, 'scripts', 'workplan.js');
const PR_LINK = path.join(ROOT, 'scripts', 'check-pr-workplan-link.js');
const SCHEMA_SRC = path.join(ROOT, 'workplan', 'schema', 'item.schema.json');

function makeBoard() {
  const dir = fs.mkdtempSync(path.join(os.tmpdir(), 'wp-'));
  fs.mkdirSync(path.join(dir, 'items'), { recursive: true });
  fs.mkdirSync(path.join(dir, 'inbox'), { recursive: true });
  fs.mkdirSync(path.join(dir, 'schema'), { recursive: true });
  fs.copyFileSync(SCHEMA_SRC, path.join(dir, 'schema', 'item.schema.json'));
  return dir;
}
function run(dir, args, extraEnv = {}) {
  return spawnSync('node', [CLI, ...args], {
    encoding: 'utf8',
    env: { ...process.env, WORKPLAN_DIR: dir, ...extraEnv },
  });
}
function runPrLink(dir, body) {
  const bodyFile = path.join(dir, 'body.md');
  fs.writeFileSync(bodyFile, body);
  return spawnSync('node', [PR_LINK, '--body-file', bodyFile], {
    encoding: 'utf8',
    env: { ...process.env, WORKPLAN_DIR: dir },
  });
}
function writeItem(dir, id, fm) {
  const y = Object.entries(fm)
    .map(([k, v]) => `${k}: ${typeof v === 'string' && /[:#]/.test(v) ? JSON.stringify(v) : v}`)
    .join('\n');
  fs.writeFileSync(path.join(dir, 'items', `${id}.md`), `---\n${y}\n---\n\nbody\n`);
}
function makeDailyNotes(dir) {
  const daily = path.join(dir, 'daily-notes');
  fs.mkdirSync(daily, { recursive: true });
  return daily;
}

const GOOD = {
  id: 'sample-good',
  title: 'Sample',
  status: 'triaged',
  type: 'infra',
  priority: 'P2',
  owner: 'claude',
  source: 'manual',
  created: '2026-06-22',
  updated: '2026-06-22',
  verification: 'a test passes',
};

test('validate passes on a well-formed item', () => {
  const dir = makeBoard();
  writeItem(dir, 'sample-good', GOOD);
  const r = run(dir, ['validate']);
  assert.equal(r.status, 0, r.stdout + r.stderr);
  assert.match(r.stdout, /1\/1 items valid/);
});

test('validate fails on bad enum, missing verification, and blocked without blocked_by', () => {
  const dir = makeBoard();
  writeItem(dir, 'sample-bad', {
    id: 'sample-bad',
    title: 'Bad',
    status: 'blocked',
    type: 'nonsense',
    priority: 'P9',
    owner: 'claude',
    source: 'manual',
    created: '2026-06-22',
    updated: '2026-06-22',
  });
  const r = run(dir, ['validate']);
  assert.equal(r.status, 1);
  assert.match(r.stdout, /type="nonsense" not in/);
  assert.match(r.stdout, /priority="P9" not in/);
  assert.match(r.stdout, /missing required field: verification/);
  assert.match(r.stdout, /blocked.*blocked_by is missing/);
});

test('validate catches id != filename', () => {
  const dir = makeBoard();
  writeItem(dir, 'wrong-name', { ...GOOD, id: 'sample-good' });
  const r = run(dir, ['validate']);
  assert.equal(r.status, 1);
  assert.match(r.stdout, /id "sample-good" != filename "wrong-name"/);
});

test('render writes board.json + BOARD.md with correct counts', () => {
  const dir = makeBoard();
  writeItem(dir, 'sample-good', GOOD);
  writeItem(dir, 'sample-two', { ...GOOD, id: 'sample-two', status: 'active' });
  const r = run(dir, ['render']);
  assert.equal(r.status, 0, r.stdout + r.stderr);
  const board = JSON.parse(fs.readFileSync(path.join(dir, 'board.json'), 'utf8'));
  assert.equal(board.counts.total, 2);
  assert.equal(board.counts.byStatus.active, 1);
  assert.equal(board.counts.byStatus.triaged, 1);
  const md = fs.readFileSync(path.join(dir, 'BOARD.md'), 'utf8');
  assert.match(md, /GENERATED by scripts\/workplan\.js/);
  assert.match(md, /## active \(1\)/);
});

test('render preserves generated timestamp when board content is unchanged', () => {
  const dir = makeBoard();
  writeItem(dir, 'sample-good', GOOD);
  const r1 = run(dir, ['render'], { WORKPLAN_RENDERED_AT: '2026-06-23T00:00:00.000Z' });
  assert.equal(r1.status, 0, r1.stdout + r1.stderr);
  const first = JSON.parse(fs.readFileSync(path.join(dir, 'board.json'), 'utf8'));
  const r2 = run(dir, ['render']);
  assert.equal(r2.status, 0, r2.stdout + r2.stderr);
  const second = JSON.parse(fs.readFileSync(path.join(dir, 'board.json'), 'utf8'));
  assert.equal(second.generated, first.generated);
});

test('check passes only when generated board files match items', () => {
  const dir = makeBoard();
  writeItem(dir, 'sample-good', GOOD);
  const r1 = run(dir, ['render'], { WORKPLAN_RENDERED_AT: '2026-06-23T00:00:00.000Z' });
  assert.equal(r1.status, 0, r1.stdout + r1.stderr);
  const ok = run(dir, ['check']);
  assert.equal(ok.status, 0, ok.stdout + ok.stderr);
  writeItem(dir, 'sample-good', { ...GOOD, title: 'Changed title' });
  const stale = run(dir, ['check']);
  assert.equal(stale.status, 1);
  assert.match(stale.stdout, /board\.json.*stale/);
  assert.match(stale.stdout, /BOARD\.md.*stale/);
});

test('ingest --daily skips self-work captures by arxiv id', () => {
  const dir = makeBoard();
  const daily = makeDailyNotes(dir);
  fs.writeFileSync(
    path.join(daily, '2026-06-15-research-roundup.html'),
    `<!doctype html>
<body>
<div class="paper hot">
  <h3>Geist in the Machine: Simulating Recognition and Inner Dialogue <span class="flag">UNBLOCK</span></h3>
  <div class="meta">Authors not retrieved - arXiv 2603.10450 - <a href="https://arxiv.org/abs/2603.10450">link</a></div>
  <p><strong>Project relevance:</strong> This is our own paper and should not become an external-work capture.</p>
</div>
<div class="paper hot">
  <h3>The Self-Correction Illusion <span class="flag">UNBLOCK</span></h3>
  <div class="meta">Kuan-Yen Chen et al. - arXiv 2606.05976 - <a href="https://arxiv.org/abs/2606.05976">link</a></div>
  <p><strong>Project relevance:</strong> External support for the ego-superego correction mechanism.</p>
</div>
</body>`,
  );
  const r = run(dir, ['ingest', '--daily'], { DAILY_NOTES_DIR: daily });
  assert.equal(r.status, 0, r.stdout + r.stderr);
  assert.match(r.stdout, /1 self-work skipped/);
  assert.match(r.stdout, /wrote 1 inbox captures/);
  const inboxFiles = fs.readdirSync(path.join(dir, 'inbox')).filter((f) => f.endsWith('.md'));
  assert.equal(inboxFiles.length, 1);
  assert.match(inboxFiles[0], /2606\.05976/);
  const body = fs.readFileSync(path.join(dir, 'inbox', inboxFiles[0]), 'utf8');
  assert.match(body, /The Self-Correction Illusion/);
  assert.doesNotMatch(body, /2603\.10450/);
});

test('PR link check accepts known item ids and explicit N/A', () => {
  const dir = makeBoard();
  writeItem(dir, 'sample-good', GOOD);
  const linked = runPrLink(dir, '## Workplan\n\n- [x] Linked workplan item: sample-good\n');
  assert.equal(linked.status, 0, linked.stdout + linked.stderr);
  assert.match(linked.stdout, /linked sample-good/);
  const na = runPrLink(dir, 'Workplan item: N/A - repo-only maintenance\n');
  assert.equal(na.status, 0, na.stdout + na.stderr);
});

test('PR link check rejects the template placeholder', () => {
  const dir = makeBoard();
  writeItem(dir, 'sample-good', GOOD);
  const r = runPrLink(dir, '- [ ] Linked workplan item: <id or N/A>\n');
  assert.equal(r.status, 1);
  assert.match(r.stderr, /must include/);
});

test('add --inbox then triage produces a valid item', () => {
  const dir = makeBoard();
  const a = run(dir, ['add', '--inbox', '--title', 'New idea worth keeping', '--source', 'manual']);
  assert.equal(a.status, 0, a.stdout + a.stderr);
  const inboxFiles = fs.readdirSync(path.join(dir, 'inbox')).filter((f) => f.endsWith('.md'));
  assert.equal(inboxFiles.length, 1);
  const t = run(dir, [
    'triage',
    path.join(dir, 'inbox', inboxFiles[0]),
    '--priority',
    'P1',
    '--owner',
    'claude',
    '--verification',
    'it ships',
  ]);
  assert.equal(t.status, 0, t.stdout + t.stderr);
  assert.equal(fs.readdirSync(path.join(dir, 'inbox')).filter((f) => f.endsWith('.md')).length, 0);
  const items = fs.readdirSync(path.join(dir, 'items'));
  assert.equal(items.length, 1);
  const v = run(dir, ['validate']);
  assert.equal(v.status, 0, v.stdout + v.stderr);
});
