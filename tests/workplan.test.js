// Hermetic tests for the workplan CLI (scripts/workplan.js).
// Runs the real CLI against a throwaway WORKPLAN_DIR so the production board is
// never touched. No DB, no network, no API.
import { test } from 'node:test';
import assert from 'node:assert/strict';
import { spawnSync } from 'node:child_process';
import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const CLI = path.join(ROOT, 'scripts', 'workplan.js');
const SCHEMA_SRC = path.join(ROOT, 'workplan', 'schema', 'item.schema.json');

function makeBoard() {
  const dir = fs.mkdtempSync(path.join(os.tmpdir(), 'wp-'));
  fs.mkdirSync(path.join(dir, 'items'), { recursive: true });
  fs.mkdirSync(path.join(dir, 'inbox'), { recursive: true });
  fs.mkdirSync(path.join(dir, 'schema'), { recursive: true });
  fs.copyFileSync(SCHEMA_SRC, path.join(dir, 'schema', 'item.schema.json'));
  return dir;
}
function run(dir, args, extraEnv = {}) {
  return spawnSync('node', [CLI, ...args], {
    encoding: 'utf8',
    env: { ...process.env, WORKPLAN_DIR: dir, ...extraEnv },
  });
}
function writeItem(dir, id, fm) {
  const y = Object.entries(fm)
    .map(([k, v]) => `${k}: ${typeof v === 'string' && /[:#]/.test(v) ? JSON.stringify(v) : v}`)
    .join('\n');
  fs.writeFileSync(path.join(dir, 'items', `${id}.md`), `---\n${y}\n---\n\nbody\n`);
}

const GOOD = {
  id: 'sample-good',
  title: 'Sample',
  status: 'triaged',
  type: 'infra',
  priority: 'P2',
  owner: 'claude',
  source: 'manual',
  created: '2026-06-22',
  updated: '2026-06-22',
  verification: 'a test passes',
};

test('validate passes on a well-formed item', () => {
  const dir = makeBoard();
  writeItem(dir, 'sample-good', GOOD);
  const r = run(dir, ['validate']);
  assert.equal(r.status, 0, r.stdout + r.stderr);
  assert.match(r.stdout, /1\/1 items valid/);
});

test('validate fails on bad enum, missing verification, and blocked without blocked_by', () => {
  const dir = makeBoard();
  writeItem(dir, 'sample-bad', {
    id: 'sample-bad',
    title: 'Bad',
    status: 'blocked',
    type: 'nonsense',
    priority: 'P9',
    owner: 'claude',
    source: 'manual',
    created: '2026-06-22',
    updated: '2026-06-22',
  });
  const r = run(dir, ['validate']);
  assert.equal(r.status, 1);
  assert.match(r.stdout, /type="nonsense" not in/);
  assert.match(r.stdout, /priority="P9" not in/);
  assert.match(r.stdout, /missing required field: verification/);
  assert.match(r.stdout, /blocked.*blocked_by is missing/);
});

test('validate catches id != filename', () => {
  const dir = makeBoard();
  writeItem(dir, 'wrong-name', { ...GOOD, id: 'sample-good' });
  const r = run(dir, ['validate']);
  assert.equal(r.status, 1);
  assert.match(r.stdout, /id "sample-good" != filename "wrong-name"/);
});

test('render writes board.json + BOARD.md with correct counts', () => {
  const dir = makeBoard();
  writeItem(dir, 'sample-good', GOOD);
  writeItem(dir, 'sample-two', { ...GOOD, id: 'sample-two', status: 'active' });
  const r = run(dir, ['render']);
  assert.equal(r.status, 0, r.stdout + r.stderr);
  const board = JSON.parse(fs.readFileSync(path.join(dir, 'board.json'), 'utf8'));
  assert.equal(board.counts.total, 2);
  assert.equal(board.counts.byStatus.active, 1);
  assert.equal(board.counts.byStatus.triaged, 1);
  const md = fs.readFileSync(path.join(dir, 'BOARD.md'), 'utf8');
  assert.match(md, /GENERATED by scripts\/workplan\.js/);
  assert.match(md, /## active \(1\)/);
});

test('add --inbox then triage produces a valid item', () => {
  const dir = makeBoard();
  const a = run(dir, ['add', '--inbox', '--title', 'New idea worth keeping', '--source', 'manual']);
  assert.equal(a.status, 0, a.stdout + a.stderr);
  const inboxFiles = fs.readdirSync(path.join(dir, 'inbox')).filter((f) => f.endsWith('.md'));
  assert.equal(inboxFiles.length, 1);
  const t = run(dir, [
    'triage',
    path.join(dir, 'inbox', inboxFiles[0]),
    '--priority',
    'P1',
    '--owner',
    'claude',
    '--verification',
    'it ships',
  ]);
  assert.equal(t.status, 0, t.stdout + t.stderr);
  assert.equal(fs.readdirSync(path.join(dir, 'inbox')).filter((f) => f.endsWith('.md')).length, 0);
  const items = fs.readdirSync(path.join(dir, 'items'));
  assert.equal(items.length, 1);
  const v = run(dir, ['validate']);
  assert.equal(v.status, 0, v.stdout + v.stderr);
});
